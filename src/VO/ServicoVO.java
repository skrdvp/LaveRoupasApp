package VO;


public class ServicoVO {
    private int codigoDoServico;
    private String descricaoDoPedido;
    private double valor;

    public int getCodigoDoServico() {
        return codigoDoServico;
    }

    public void setCodigoDoServico(int codigoDoServico) {
        this.codigoDoServico = codigoDoServico;
    }

    public String getDescricaoDoPedido() {
        return descricaoDoPedido;
    }

    public void setDescricaoDoPedido(String descricaoDoPedido) {
        this.descricaoDoPedido = descricaoDoPedido;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
            
}
